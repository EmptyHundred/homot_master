// ---------------------------------------------------------------------------
// GDScript module init/deinit
// ---------------------------------------------------------------------------

static GDScriptLanguage *gd_language = nullptr;
static Ref<ResourceFormatLoaderGDScript> gd_loader;
static Ref<ResourceFormatSaverGDScript> gd_saver;

static void init_gdscript_module() {
	GDREGISTER_CLASS(GDScript);
	gd_language = memnew(GDScriptLanguage);
	ScriptServer::register_language(gd_language);
	gd_loader.instantiate();
	ResourceLoader::add_resource_format_loader(gd_loader);
	gd_saver.instantiate();
	ResourceSaver::add_resource_format_saver(gd_saver);
	GDScriptUtilityFunctions::register_functions();
}

static void deinit_gdscript_module() {
	GDScriptUtilityFunctions::unregister_functions();
	ResourceLoader::remove_resource_format_loader(gd_loader);
	gd_loader.unref();
	ResourceSaver::remove_resource_format_saver(gd_saver);
	gd_saver.unref();
	ScriptServer::unregister_language(gd_language);
	memdelete(gd_language);
	gd_language = nullptr;
}

// ---------------------------------------------------------------------------
// Engine bootstrap / cleanup
// ---------------------------------------------------------------------------

static Engine *engine_inst = nullptr;
static ProjectSettings *project_settings_inst = nullptr;
static TranslationServer *translation_server_inst = nullptr;
static TextServerManager *text_server_manager_inst = nullptr;
static GDScriptCache *gdscript_cache_inst = nullptr;

static void engine_bootstrap() {
	engine_inst = memnew(Engine);
	register_core_types();
	register_platform_file_access();

	project_settings_inst = memnew(ProjectSettings);
	register_core_settings();

	translation_server_inst = memnew(TranslationServer);

	text_server_manager_inst = memnew(TextServerManager);
	{
		Ref<TextServerDummy> ts;
		ts.instantiate();
		text_server_manager_inst->add_interface(ts);
		text_server_manager_inst->set_primary_interface(ts);
	}

	register_early_core_singletons();
	register_core_singletons();

	init_gdscript_module();
	gdscript_cache_inst = memnew(GDScriptCache);

	initialize_holymolly_module(MODULE_INITIALIZATION_LEVEL_SERVERS);
}

static void engine_cleanup() {
	if (linter::LinterDB::get_singleton()) {
		memdelete(linter::LinterDB::get_singleton());
	}
	uninitialize_holymolly_module(MODULE_INITIALIZATION_LEVEL_SERVERS);
	GDScriptCache::clear();
	memdelete(gdscript_cache_inst);
	deinit_gdscript_module();

	memdelete(text_server_manager_inst);
	memdelete(translation_server_inst);
	memdelete(project_settings_inst);
	memdelete(engine_inst);

	unregister_core_types();
}

// ---------------------------------------------------------------------------
// Load linterdb: from --db flag or embedded data.
// ---------------------------------------------------------------------------

static bool load_linterdb(const char *db_path_override) {
	linter::LinterDB *db = memnew(linter::LinterDB);
	Error err;

	if (db_path_override) {
		err = db->load_from_json(String::utf8(db_path_override));
	} else {
		err = db->load_from_compressed(linterdb_compressed_data, linterdb_compressed_size, linterdb_uncompressed_size);
	}

	if (err != OK) {
		memdelete(db);
		return false;
	}
	return true;
}

// ---------------------------------------------------------------------------
// Subcommand: lint
// ---------------------------------------------------------------------------

static int cmd_lint(int argc, char *argv[], const char *db_override) {
	static const int MAX_TARGETS = 256;
	const char *targets[MAX_TARGETS];
	int target_count = 0;

	for (int i = 0; i < argc; i++) {
		if (argv[i][0] == '-') {
			fprintf(stderr, "Unknown lint option: %s\n", argv[i]);
			return 1;
		} else {
			if (target_count < MAX_TARGETS) {
				targets[target_count++] = argv[i];
			}
		}
	}

	if (target_count == 0) {
		fprintf(stderr, "ERROR: No files or directories specified.\nUsage: homot lint [--db <path>] <path> [<path> ...]\n");
		return 1;
	}

	Vector<String> lint_paths;
	for (int i = 0; i < target_count; i++) {
		lint_paths.push_back(String::utf8(targets[i]));
	}

	// Load DB via the shared loader (uses embedded or override).
	if (!load_linterdb(db_override)) {
		fprintf(stderr, "ERROR: Failed to load linterdb.\n");
		return 1;
	}

	// run_lint with empty db_path since DB is already loaded.
	int result = linter::run_lint(lint_paths, String());
	return result;
}

// ---------------------------------------------------------------------------
// Subcommand: lsp
// ---------------------------------------------------------------------------

static int cmd_lsp(const char *db_override) {
	if (!load_linterdb(db_override)) {
		fprintf(stderr, "ERROR: Failed to load linterdb.\n");
		return 1;
	}

	lsp::Server server;
	// DB is already loaded; set empty path so server doesn't try to reload.
	server.set_db_path(String());

	while (true) {
		Dictionary msg = lsp::Transport::read_message();
		if (msg.is_empty()) {
			break;
		}
		if (!server.process_message(msg)) {
			break;
		}
	}

	return 0;
}

// ---------------------------------------------------------------------------
// Subcommand: lspa
// ---------------------------------------------------------------------------

static int cmd_lspa(const char *db_override) {
	if (!load_linterdb(db_override)) {
		fprintf(stderr, "ERROR: Failed to load linterdb.\n");
		return 1;
	}

	lspa::Server server;
	server.set_db_path(String());

	while (true) {
		Dictionary msg = lsp::Transport::read_message();
		if (msg.is_empty()) {
			break;
		}
		if (!server.process_message(msg)) {
			break;
		}
	}

	return 0;
}

// ---------------------------------------------------------------------------
// Help
