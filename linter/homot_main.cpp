// ---------------------------------------------------------------------------
// Suppress stderr during static initialization.
// ---------------------------------------------------------------------------

static int _homot_saved_stderr_fd = -1;

#ifdef _WIN32
#pragma init_seg(compiler)
static struct _HomotStderrGuard {
	_HomotStderrGuard() {
		fflush(stderr);
		_homot_saved_stderr_fd = _dup(_fileno(stderr));
		FILE *nul = fopen("NUL", "w");
		if (nul) {
			_dup2(_fileno(nul), _fileno(stderr));
			fclose(nul);
		}
	}
} _homot_stderr_guard;
#else
__attribute__((constructor(101)))
static void _homot_suppress_stderr() {
	fflush(stderr);
	_homot_saved_stderr_fd = dup(fileno(stderr));
	FILE *nul = fopen("/dev/null", "w");
	if (nul) {
		dup2(fileno(nul), fileno(stderr));
		fclose(nul);
	}
}
#endif

static void _homot_restore_stderr() {
	if (_homot_saved_stderr_fd >= 0) {
		fflush(stderr);
#ifdef _WIN32
		_dup2(_homot_saved_stderr_fd, _fileno(stderr));
		_close(_homot_saved_stderr_fd);
#else
		dup2(_homot_saved_stderr_fd, fileno(stderr));
		close(_homot_saved_stderr_fd);
#endif
		_homot_saved_stderr_fd = -1;
	}
}

